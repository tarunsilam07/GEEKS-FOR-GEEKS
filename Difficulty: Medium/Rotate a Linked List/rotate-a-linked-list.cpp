class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        if(head==NULL || head->next==NULL) return head;
       Node* temp=head;
       int n=0;
       Node* last=NULL;
       while(temp)
       {
           n++;
           if(temp->next==NULL)
           last=temp;
           temp=temp->next;
           
       }
       k=k%n;
       if(k==0) return head;
       int cnt=1;
       Node* knode=NULL;
       temp=head;
       while(temp)
       {
           if(cnt==k)
           knode=temp;
           cnt++;
           temp=temp->next;
       }
       Node* newhead=knode->next;
       knode->next=NULL;
       last->next=head;
       return newhead;
    }
};
    



