class Solution {
  public:
    vector<int> intersectionWithDuplicates(vector<int>& a, vector<int>& b) {
        unordered_map<int,int>hash1;
        unordered_map<int,int>hash2;
        for(auto it:a)
        hash1[it]++;
        
        for(auto it:b)
        hash2[it]++;
        
        vector<int>ans;
        for(auto it:hash1){
            if(hash2[it.first]>0)
            ans.push_back(it.first);
        }
        return ans;
        
    }
};

